function toNumber(value) {
  const n = Number(value);
  return Number.isFinite(n) ? n : 0;
}

function normalizeMealType(value) {
  return String(value || "").trim().toLowerCase();
}

function passesFilters(item, options) {
  if (options?.portableOnly && !item.portable) return false;
  if (options?.vegetarianOnly && !item.vegetarian) return false;
  if (options?.quickOnly && !item.quick) return false;

  const maxCalories = toNumber(options?.maxCalories);
  if (maxCalories > 0 && item.calories > maxCalories) return false;

  const wantedMealType = normalizeMealType(options?.mealType);
  if (wantedMealType) {
    const allowedTypes = Array.isArray(item.mealType) ? item.mealType.map(normalizeMealType) : [];
    if (!allowedTypes.includes(wantedMealType)) return false;
  }

  return true;
}

function scoreSuggestion(item, currentVegetableServings, options) {
  const current = Math.max(0, toNumber(currentVegetableServings));
  const missingServings = Math.max(0, 3 - current);
  const servingsGap = Math.abs(item.vegetableServings - missingServings);
  let score = 0;

  score += item.vegetableServings * 12;
  score += Math.max(0, 18 - servingsGap * 8);

  if (item.calories <= 220) score += 5;
  else if (item.calories <= 320) score += 2;
  else score -= 1.5;

  if (item.quick) score += 3;
  if (item.protein >= 8) score += 2;
  if (item.protein >= 18) score += 1.5;
  if (item.highFiber) score += 1.5;
  if (item.lowCalorie) score += 1.5;

  if (options?.portableOnly && item.portable) score += 4;
  if (options?.quickOnly && item.quick) score += 3;
  if (options?.vegetarianOnly && item.vegetarian) score += 2;

  return Number(score.toFixed(2));
}

export function getVegetableRecoverySuggestions(currentVegetableServings, options = {}) {
  const current = Math.max(0, toNumber(currentVegetableServings));
  if (current >= 3) return [];

  return vegetableRecoveryFoods
    .filter((item) => passesFilters(item, options))
    .map((item) => ({
      ...item,
      _score: scoreSuggestion(item, current, options),
    }))
    .sort((a, b) => {
      if (b._score !== a._score) return b._score - a._score;
      if (b.vegetableServings !== a.vegetableServings) {
        return b.vegetableServings - a.vegetableServings;
      }
      return a.calories - b.calories;
    })
    .slice(0, 8)
    .map(({ _score, ...item }) => item);
}
