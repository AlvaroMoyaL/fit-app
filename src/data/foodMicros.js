export function applyMicronutrientProfile(food) {
  const safe = food || {};
  const key = normalize(safe.name);
  const categoryKey = normalize(safe.category);
  const profileKey = DIRECT_MAP[key] || CATEGORY_DEFAULTS[categoryKey] || null;
  const profile = profileKey ? PROFILES[profileKey] : null;
  if (!profile) return safe;

  return {
    ...safe,
    sodium: Number(safe.sodium ?? profile.sodium ?? 0),
    sugars: Number(safe.sugars ?? profile.sugars ?? 0),
    fiber: Number(safe.fiber ?? profile.fiber ?? 0),
    saturatedFat: Number(safe.saturatedFat ?? profile.saturatedFat ?? 0),
    cholesterol: Number(safe.cholesterol ?? profile.cholesterol ?? 0),
    transFat: Number(safe.transFat ?? profile.transFat ?? 0),
  };
}
